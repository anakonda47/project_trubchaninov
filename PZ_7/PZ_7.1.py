#Дана строка, изображающая арифметическое выражение вида
#«<цифра>1<цифра>t.. І<цифра»», где на месте знака операции «-» находится
#символ «+» или «-» (например, «4+7-2 — 8»). Вывести значение данного выражения (целое число).

try:
    s = input().strip()
    result = int(s[0])  # первая цифра

    for i in range(1, len(s), 2):
        op = s[i]       # оператор (+ или -)
        num = int(s[i+1])  # следующая цифра
        if op == '+':
            result += num
        else:  # op == '-'
            result -= num

    print(result)
except ValueError:
    print("Ошибка")
